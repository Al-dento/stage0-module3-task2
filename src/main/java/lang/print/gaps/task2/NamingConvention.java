package lang.print.gaps.task2;

public class NamingConvention {
    static final int VARIABLE_FOUR = 4;
    static final int VARIABLE_FIVE = 5;
    public static void main(String[] args) {
        int variableZero = 10;
        int variableOne = 1;
        int variableTwo = 2;
        int variableThree = 3;
        int variableSix = 6;
        int variableSeven = 7;
        int variableEight = 8;

        System.out.println(VARIABLE_FOUR + " " + VARIABLE_FIVE + " " + variableZero + " " + variableOne
                + " " + variableTwo + " " + variableThree + " " + variableSix + " " + variableSeven + " " + variableEight);

    }
}
